    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("views/pages/forgotPassword.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            CustomerService cs = new CustomerService();
            String email = request.getParameter("email").trim();

            if (!email.matches("[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?")) {
                request.setAttribute("msg", "Email invalid!!!");
                request.getRequestDispatcher("views/pages/forgotPassword.jsp").forward(request, response);
            } else if (cs.checkEmailExist(email)) {
                String code = SendMail.getRandom();
                HashMap<String, String> verti = new HashMap<String, String>();
                verti.put(email, code);
                HttpSession ss = request.getSession();
                ss.setAttribute("verti", verti);
                System.out.println((HashMap<String, String>) ss.getAttribute("verti"));
                if (SendMail.sendEmail(email, code,AESEncryption.encrypt(email))) {
                    request.getRequestDispatcher("views/pages/checkMail.jsp").forward(request, response);
                } else {
                    request.getRequestDispatcher("views/pages/sendFail.jsp").forward(request, response);
                }

            } else {
                request.setAttribute("msg", "Account not Exist!");
                request.getRequestDispatcher("views/pages/forgotPassword.jsp").forward(request, response);
            }
        } catch (Exception ex) {
            Logger.getLogger(ForgotPasswordController.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

